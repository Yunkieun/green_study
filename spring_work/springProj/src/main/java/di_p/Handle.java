package di_p;

public class Handle {
	
	String name;
	
	public Handle() {
		System.out.println("Handle 기본생성자");
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		System.out.println("Handel setName():"+name);
		this.name = name;
	}

	@Override
	public String toString() {
		return "Handle [name=" + name + "]";
	}
	
	
}
